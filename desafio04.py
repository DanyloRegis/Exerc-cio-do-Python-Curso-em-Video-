
n1=input("Digite um dado: ")

print("O dado digitado e alfanumerico ", n1.isalnum())
print("O dado digitado e numerico ", n1.isnumeric())
print("O dado digitado e alfabeto ", n1.isalpha())
print("O dado digitado e decimal ", n1.isdecimal())
print("O dado digitado e digito ", n1.isdigit())
print("O dado digitado e minusculo ", n1.islower())
print("O dado digitado e maiusculo ", n1.isupper())   
print("O dado digitado e simbolo ", n1.isidentifier())
print("O dado digitado e espaco ", n1.isspace())